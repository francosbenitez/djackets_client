<template> 
  <div class="">
    <div class="">
      <h1>My Account</h1>
    </div>

    <div class="">
      <button @click="logout">
        Log out
      </button>
    </div>
  </div>
</template>

<script>
import axios from 'axios'

export default {
  name: 'MyAccount',
  methods: {
    logout() {
      axios.defaults.headers.common["Authorization"]

      localStorage.removeItem("token")
      localStorage.removeItem("username")
      localStorage.removeItem("userid")

      this.$store.commit('removeToken')
      this.$router.push('/')
    }
  }
}
</script>
